# age convertion and next birthday date
age = int(input("Enter your age?: "))
next_birthday_age = age + 1
print(f"Next year, you will be {next_birthday_age} years old.")

# number of eggs in a carton
num_eggs_carton =  input("how many eggs cartons do you have?: ")
eggs_per_carton = num_eggs_carton * 12
print(f"You have {eggs_per_carton} eggs in total.")

#  number of cookies and people
num_cookies = int(input("Enter the number of cookies: "))
num_people = int(input("Enter the number of people: "))
cookies_per_person = num_cookies // num_people
print(f"Each person gets {cookies_per_person} cookies.")

